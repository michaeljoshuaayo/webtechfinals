import { User, Tooltip, Chip } from "@nextui-org/react";
import React from "react";
import { DeleteIcon } from "../icons/table/delete-icon";
import { EditIcon } from "../icons/table/edit-icon";
import { EyeIcon } from "../icons/table/eye-icon";
import { bloods } from "./data";

interface Props {
  blood: (typeof bloods)[number];
  columnKey: string | React.Key;
}

export const RenderCell = ({ blood, columnKey }: Props) => {
  // @ts-ignore
  const cellValue = blood[columnKey];
  switch (columnKey) {
    case "name":
      return (
        <User
          name={cellValue}
        >
          {blood.bloodSerialNum}
        </User>
      );
    case "role":
      return (
        <div>
          <div>
            <span>{cellValue}</span>
          </div>
          <div>
            <span>{blood.bloodSerialNum}</span>
          </div>
        </div>
      );
    case "status":
      return (
        <Chip
          size="sm"
          variant="flat"
          color={
            cellValue === "Available" ? "success" : "primary"
          }
        >
          <span className="capitalize text-xs">{cellValue}</span>
        </Chip>
      );

    case "actions":
      return (
        <div className="flex items-center gap-4 ">
          <div>
            <Tooltip content="Details">
              <button onClick={() => console.log("View blood", blood.id)}>
                <EyeIcon size={20} fill="#979797" />
              </button>
            </Tooltip>
          </div>
          <div>
            <Tooltip content="Edit blood" color="secondary">
              <button onClick={() => console.log("Edit blood", blood.id)}>
                <EditIcon size={20} fill="#979797" />
              </button>
            </Tooltip>
          </div>
          <div>
            <Tooltip
              content="Delete blood"
              color="danger"
              onClick={() => console.log("Delete blood", blood.id)}
            >
              <button>
                <DeleteIcon size={20} fill="#FF0080" />
              </button>
            </Tooltip>
          </div>
        </div>
      );
    default:
      return cellValue;
  }
};
